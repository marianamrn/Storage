bool CONSOLE::LOGIN() {
	bool loggedIn = false;
	do {
		cout << "--------------------------------------------------" << endl
			<< "|               ВХІД В СИСТЕМУ                   |" << endl
			<< "--------------------------------------------------" << endl;
		cout << "| Логін | => ";
		string login = ConsoleIO::GetText(cin);
		cout << "| Пароль | => ";
		string password = ConsoleIO::GetText(cin);
		loggedIn = system.logIn(login, password);
		cout << endl;
		Clear();
		if (loggedIn) {
			cout << "Ви увійшли в систему як " << login << "!" << endl;
			return true;
		}
		else {
			cout 
				<< "---------------------------------------------------" << endl
				<< "|                  ПОМИЛКА                        |" << endl
				<< "---------------------------------------------------" << endl
				<< "| Неправильний логін чи пароль!                   |" << endl
				<< "---------------------------------------------------" << endl << endl;
		}
	} while (!loggedIn);
	return false;
}

